package com.capstone.merkado.Objects.FactoryDataObjects;

public class FactoryDefaults {
    Long proficiencyRequirement;
    Long proficiencyReward;

    public FactoryDefaults() {
    }

    public Long getProficiencyRequirement() {
        return proficiencyRequirement;
    }

    public void setProficiencyRequirement(Long proficiencyRequirement) {
        this.proficiencyRequirement = proficiencyRequirement;
    }

    public Long getProficiencyReward() {
        return proficiencyReward;
    }

    public void setProficiencyReward(Long proficiencyReward) {
        this.proficiencyReward = proficiencyReward;
    }
}
